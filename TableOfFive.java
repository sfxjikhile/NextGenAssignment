public class TableOfFive {
    public static void main(String[] args){
        for (int i = 5; i <=5; i++){
            System.out.println("Table of 5");
            for(int j = 1; j <= 10; j++){
                System.out.println(i + "*" + j + " = " + (i*j));
        }

        }
    }

}

