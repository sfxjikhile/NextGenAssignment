package com.assignment.solutions.assignment8.managementSystem;

public class ManagementSystem {
     private String author;
     private String title;
     private double price;
     private int stock;
     private double softwareVersion;
     private String softwareName;
     private int numberOfPages;
     private String hardwareCategory;
     private String publisher;

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public double getSoftwareVersion() {
        return softwareVersion;
    }

    public void setSoftwareVersion(double softwareVersion) {
        this.softwareVersion = softwareVersion;
    }

    public String getSoftwareName() {
        return softwareName;
    }

    public void setSoftwareName(String softwareName) {
        this.softwareName = softwareName;
    }

    public int getNumberOfPages() {
        return numberOfPages;
    }

    public void setNumberOfPages(int numberOfPages) {
        this.numberOfPages = numberOfPages;
    }

    public String getHardwareCategory() {
        return hardwareCategory;
    }

    public void setHardwareCategory(String hardwareCategory) {
        this.hardwareCategory = hardwareCategory;
    }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }
}
