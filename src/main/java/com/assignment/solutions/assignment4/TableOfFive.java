package com.assignment.solutions.assignment4;

public class TableOfFive {
    public static void main(String[] args) {
        int num = 5;

        // Print the table of 5
        for (int i = 1; i <= 12; i++) {
            System.out.println(num + " x " + i + " = " + (num * i));
        }
    }
}
